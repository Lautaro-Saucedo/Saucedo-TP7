    private void jlNombresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlNombresMouseClicked
        borrarLista();
        try {
            Alumno a = jlNombres.getSelectedValue();
            for(Materia m:a.getMaterias()){
                model.addRow(new Object[]{m.getIdMateria(),m.getNombre(),m.getAnio()});
            }
            jtMaterias.setModel(model);
        } catch(NullPointerException npe){
            JOptionPane.showMessageDialog(this, "No se encontraron alumnos para mostrar.");
        }
    }//GEN-LAST:event_jlNombresMouseClicked

