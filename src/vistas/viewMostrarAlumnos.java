    private void llenarLista(){
        DefaultListModel<Alumno> lista=new DefaultListModel<>();
        for (Alumno a:setalu){
            lista.addElement(a);
        }
        jlNombres.setModel(lista);
    }
    
    private void cabecera(){
        ArrayList<Object> col = new ArrayList<>();
        col.add("ID");
        col.add("Nombre");
        col.add("Año");
        
        for (Object a:col){
            model.addColumn(a);
        }
        jtMaterias.setModel(model);
    }
    
    private void borrarLista(){
        for (int a=model.getRowCount()-1;a>=0;a--){
            model.removeRow(a);
        }
    }

