    private void llenarListaAlumnos(){
        for (Alumno a:setalu){
            jcbAlumno.addItem(a);
        }
    }
    
    private void llenarListaMaterias(){
        for (Materia a:setmat){
            jcbMateria.addItem(a);
        }
    }

